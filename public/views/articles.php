            <h2>Les metaverses : le futur d'aujourd'hui</h2>
            <h3>Comment les réseaux sociaux vont vite être remplacés par les métaverses</h3>

            <img class="article-img-1" src="images/metaversesarticle.png" alt="">
            <figcaption>Un monde digital en 3D</figcaption>

            <p>
                Aujourd'hui l'internet 2.0 que nous connaissons tous a fait bien plus que ses preuves. Les réseaux sociaux font parti inhérente de nos vies et constitue un socle commun de la plupart des sociétés occidentales. Avec l'arrivée des cryptomonnaies mais aussi plus récemment des NFT (Non Fungible Token), internet prend un virage impressionant vers la digitalisation complète. Les métaverses ces univers en 3D en sont la preuve..
            </p>



            <p>
                Facebook vous connaissez comme 3 milliards d'utilisateurs sans aucun doute. Et bien sachez que la firme de Zuckerberg s'appelle dorénavant Meta. Bien évidemment le diminutif de "métaverse". Car la société au pouce bleu a vite compris que tout se jouer dans ces nouveaux mondes virtuels où vous avez la possibilité de visiter un monde juste à l'aide d'un casque virtuel.
            </p>

            <h4>"Toutes les grande sociétés investissent déjà dans ces mondes"</h4>
            <figcaption>Phil Johnson, CEO d'une startup côté en bourse</figcaption>

            <img class="article-img-2" src="images/metaversesarticle2.jpg" alt="">
            <figcaption>Vous pouvez dorénavant faire des confcall en 3D</figcaption>

            <p>Phil Johnson, par le biais de sa société a déjà investi dans ces nouveaux metaverses</p>

            <p class="text-question">Comment est-ce que l'avenir se dessine selon vous ?</p>

            <p>Phil Johnson : C'est très simple, vous avez dorénavant de gros Metaverses comme Meta, Sandbox qui propose des parcelles de terrain a des montant astronomiques. Avoir une place dans ces univers contribue a devenir une carte postale digitale. Les sites seront toujours présents mais une maison dans les métaverses constituera l'avenir de demain</p>

            <p class="text-question"> Comment expliquez vous cet entrain ? </p>

            <p>Phil Johnson : L'arrivée du Bitcoin il y a de cela 10 ans a grandement contribué a tout ce buzz médiatique. Mais je pense que c'est réellement les NFT avec la vente d'oeuvres d'arts digitalisés qui a fait comprendre a un grand nombre de personne que l'on pouvait digitaliser de nombreux items </p>

            <p class="text-question"> Conseillez-vous aux jeunes d'investir dedans ? </p>

            <p> Phil Johnson : Je pense qu'il faut néanmoins être prudent et ne pas sombrer dans la spéculation. Bien évidemment que les enjeux financiers s'y joueront prochainement, néanmoins il faut toujours y aller avec parcimonie. </p>


